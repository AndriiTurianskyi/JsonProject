package Model;

public class Device {
    private String brandName;
    private String code;


    // Getter Methods

    public String getBrandName() {
        return brandName;
    }

    public String getCode() {
        return code;
    }

    // Setter Methods

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
